package mechanics;

/**
 * @author Shaan
 */
public class Square {
    private int x;
    private int y;

    public Square(int xco, int yco) {
        this.x = xco;
        this.y = yco;
    }
    
    
    public int getX(){
        return this.x;
    }
    
    public int getY(){
        return this.y;
    }
    
}
