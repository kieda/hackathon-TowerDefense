public class Square {
    private int x, y;
    
    public Square(int x, int y){this.x=x; this.y=y;}
    
    /**
     * returns the x position of this square
     * @return 
     */
    public int getX(){return x;}
    /**
     * returns the y position of this square
     * @return 
     */
    public int getY(){return y;}
}
